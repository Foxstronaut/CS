public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }

    public Point translate(int dx, int dy) {
        return new Point(this.x + dx, this.y + dy);
    }
}